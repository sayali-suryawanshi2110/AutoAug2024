package Variables;

public class sample1 
{
	public static void main(String[] args)
	{
		//variable declaration(allocating memory)
	String name;
	int rollnum;                      //datatype  variablename
	float percentage;
	char grade;
	
	//variable initialisation(assigning/inserting value)
		
	name= "sayali";
	rollnum=301;        //variablename = "variable value"
	percentage=85.5f;
	grade='A';
	
	//variable usage
	System.out.println(name);
	System.out.println(rollnum);
	System.out.println(percentage);
	System.out.println(grade);      //info reuse
	System.out.println(grade);
	
	
		
	}

}
